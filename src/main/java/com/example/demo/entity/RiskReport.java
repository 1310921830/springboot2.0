package com.example.demo.entity;

public class RiskReport {
    private String id;
    
    private String title;
    
    private String date;

    private String riskDescribe;

    private String probability;

    private String influence;

    private String riskLevel;

    private String plan;

    private String riskOwner;

    private String projectId;

    private String isCheck;

    private String isPass;

    private String opinion;

    private String link;

    private String preparedName;

    private String reviewedName;
    
    public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getRiskDescribe() {
		return riskDescribe;
	}

	public void setRiskDescribe(String riskDescribe) {
		this.riskDescribe = riskDescribe;
	}

	public String getProbability() {
		return probability;
	}

	public void setProbability(String probability) {
		this.probability = probability;
	}

	public String getInfluence() {
		return influence;
	}

	public void setInfluence(String influence) {
		this.influence = influence;
	}

	public String getRiskLevel() {
		return riskLevel;
	}

	public void setRiskLevel(String riskLevel) {
		this.riskLevel = riskLevel;
	}

	public String getPlan() {
		return plan;
	}

	public void setPlan(String plan) {
		this.plan = plan;
	}

	public String getRiskOwner() {
		return riskOwner;
	}

	public void setRiskOwner(String riskOwner) {
		this.riskOwner = riskOwner;
	}

	public String getProjectId() {
		return projectId;
	}

	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	public String getIsCheck() {
		return isCheck;
	}

	public void setIsCheck(String isCheck) {
		this.isCheck = isCheck;
	}

	public String getIsPass() {
		return isPass;
	}

	public void setIsPass(String isPass) {
		this.isPass = isPass;
	}

	public String getOpinion() {
		return opinion;
	}

	public void setOpinion(String opinion) {
		this.opinion = opinion;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getPreparedName() {
		return preparedName;
	}

	public void setPreparedName(String preparedName) {
		this.preparedName = preparedName;
	}

	public String getReviewedName() {
		return reviewedName;
	}

	public void setReviewedName(String reviewedName) {
		this.reviewedName = reviewedName;
	}

	

   
}